/**
 * O método estático sign retorna o sinal do número.
 * 
 * Se o número for negativo o método retornará -1.
 * Se o número for zero o método retornará 0.
 * Se o número for "-zero" o método retornará -0.
 * Se o número for positivo o método retornará 1.
 * 
 * Se o parâmetro não for um número o método retornará NaN.
 */

console.log(Math.sign(-5));
console.log(Math.sign(0));
console.log(Math.sign(5));
