/**
 * O método estático atanh retorna o arco tangente hiperbólico de um número.
 * 
 * Se o parâmetro passado for menor que -1 ou maior que 1 o método retornará
 * NaN.
 * 
 * Se o parâmetro for 1 o método retornará Infinity (infinito).
 * Se o parâmetro for -1 o método retornará -Infinity (-infinito).
 */

console.log(Math.atanh(0.5));
console.log(Math.atanh(1));
