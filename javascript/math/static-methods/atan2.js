/**
 * O método estático atan2 retorna o arco tangente do quociente de seus
 * parâmetros. O retorno é dado em radianos.
 * 
 * O número retornado representa o ângulo no sentido anti-horário entre o ponto
 * (x, y) e o eixo x positivo. O método recebe os parâmetros x e y na ordem
 * inversa, ou seja (y, x).
 * 
 * Se o parâmetro passado for vazio o método retornará NaN.
 */

console.log(Math.atan2(8, 4));
 