/**
 * o método includes verifica se um determinado elemento está contido dentro de
 * um array e retorna um valor booleano
 *
 * o método includes recebe 2 parâmeteros:
 *
 *   1° - value (obrigatório): valor a ser verificado
 *   2° - start (opcional): índice inicial da verificação
 */

const fruits = ["Banana", "Orange", "Apple", "Mango"];

console.log("Resultado: ", fruits.includes("Banana", 2));
