/**
 * o método estático isArray verifica se a variável é um array e retorna um
 * valor booleano
 * 
 * o método estático isArray recebe 1 parâmetro:
 * 
 *   1° - object (obrigatório): objeto a ser verificado
 */

let fruits = ["Banana", "Orange", "Apple", "Mango"];

console.log("Resultado: ", Array.isArray(fruits));
